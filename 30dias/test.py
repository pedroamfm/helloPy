my_list = [1, 2, 3, 4, 5, 6, 7]

first_half = my_list[:len(my_list) // 2]  # Get the first half
second_half = my_list[len(my_list) // 2:]  # Get the second half

print("First half:", first_half)  # Output: First half: [1, 2, 3]
print("Second half:", second_half)  # Output: Second half: [4, 5, 6, 7]

